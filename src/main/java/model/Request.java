package model;

/**
 *
 * @author pc
 */
public class Request {
    private int id, serviceId, accountId, requestStatusId;
    private String timestamp, description;

    public Request() {
    }

    
    public Request(int id, int serviceId, int accountId, int requestStatusId, String timestamp, String description) {
        this.id = id;
        this.serviceId = serviceId;
        this.accountId = accountId;
        this.requestStatusId = requestStatusId;
        this.timestamp = timestamp;
        this.description = description;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getServiceId() {
        return serviceId;
    }

    public void setServiceId(int serviceId) {
        this.serviceId = serviceId;
    }

    public int getAccountId() {
        return accountId;
    }

    public void setAccountId(int accountId) {
        this.accountId = accountId;
    }

    public int getRequestStatusId() {
        return requestStatusId;
    }

    public void setRequestStatusId(int requestStatusId) {
        this.requestStatusId = requestStatusId;
    }

    public String getTimestamp() {
        return timestamp;
    }

    public void setTimestamp(String timestamp) {
        this.timestamp = timestamp;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }
    
}
